-- Three types of operators
-- #############################

-- Operators are special kekywords in SQL that we use in conjunction with SQL clauses to:

-- 	* Compare the values of fields
--	* Select subsets of field
--	* Perform arithmetic operations

-- Types of Operators

-- 1. Comparison
-- #############################

-- Equal to			=
-- Greater than		>
-- Less than		<
-- Greater or equal	>=
-- Less or equal 	<=
-- Not equal		<>

-- 2. Logical Operators
-- #########################

-- AND
-- OR
-- LIKE
-- IN 
-- BETWEEN

-- 3. Arithmetic
-- #########################

-- Addition			+
-- Subtraction		-
-- Division			/
-- Multiplication	*
-- Modulo			%